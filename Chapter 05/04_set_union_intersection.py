
set1 = {1,4,5,2,7}
set2 = {5,7,9,12,19}

#! Union mean it merge the above two but doesnot repeat the same element

print(set1.union(set2))              #? it return the union of two sets

#! Intersection mean it return the common element between the two sets

print(set1.intersection(set2))       #? it return the intersection of two sets


print(set1.difference(set2))         #? it return the difference of two sets