#! We iterate for loop in python in tuple, list and string
#* For list:

list = ["Moin","muhammad","khan","abdullah",1 , True]
for i in list:
    print(i)

#* For tuple:

tuple = ("ahmed", 54 , False , "hey" , 32.2)
for j in tuple:
    print(j)

#* For String

str = "Muhammad"
for k in str:
    print(k)


