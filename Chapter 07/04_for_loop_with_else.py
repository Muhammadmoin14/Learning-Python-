
#! FOR LOOP WITH ELSE
#! When value inside for loop is not satisfying the condition then else block will be executed.

list =["Moin","muhammad","khan","abdullah",1 , True]

for i in list :
    print(i)
else:                       #? else block will be executed when for loop end without satisfying the condition
    print("Done")

