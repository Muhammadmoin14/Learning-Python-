
#! Question 01
#! Create two virtual environments, install few packages in the first one. How do you create a similar environment in the second one?

#? Step 1: Create two virtual environments by using "virtualenv enviroment_01 , virtualenv enviroment_02"
#? Step 2: Activate the first virtual environment and install packages by using ".\enviroment_01\Scripts\activate.bat"
#? Step 3: Create a environment file by using "pip freeze requirement.txt"
#? Step 4: Deactivate the first virtual environment by using "deactivate"
#? Step 5: Activate the second environment by using ".\enviroment_02\Scripts\activate.bat"
#? Step 6: Create a similar environment in the second one by using "pip install -r requirement.txt"
